#![allow(non_snake_case)]

pub struct Timestep
{
    m_Time: f32         // in seconds
}

impl Timestep
{
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    pub fn Create(time: f32) -> Timestep
    {
        return Timestep { m_Time: time };
    }

    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    pub fn GetSeconds(&self) -> f32
    {
        return self.m_Time;
    }

    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    pub fn GetMilliSeconds(&self) -> f32
    {
        return self.m_Time * 1000.0;
    }
}